const typeDefs = gql`
  enum YesNo {
    YES
    NO
  }

  type Author {
    name: String!
    born: Int
    id: ID!
    bookCount: Int
  }

  type Book {
    title: String!
    published: Int!
    author: String!
    id: ID!
    genres: [String]
  }

  type Query {
    authorCount: Int!
    bookCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors(born: YesNo): [Author!]!
    findAuthor(name: String!): Author
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String]
    ): Book
    editBorn(name: String!, born: Int!): Author
  }
`;

const resolvers = {
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allBooks: (root, args) => {
      if (args.author && args.genre) {
        return books
          .filter((book) => book.author === args.author)
          .filter((book) => book.genres.includes(args.genre));
      } else if (args.author) {
        return books.filter((book) => book.author === args.author);
      } else if (args.genre) {
        return books.filter((book) => book.genres.includes(args.genre));
      }
      return books;
    },
    allAuthors: (root, args) => {
      if (!args.born) {
        return authors;
      }
      const byBorn = (author) =>
        args.born === "YES" ? author.born : !author.born;
      return authors.filter(byBorn);
    },
    findAuthor: (root, args) => authors.find((a) => a.name === args.name),
  },
  Author: {
    bookCount: (root) => {
      return books.filter((book) => book.author === root.name).length;
    },
  },
  Mutation: {
    addBook: (root, args) => {
      if (books.find((book) => book.title === args.title)) {
        throw new UserInputError("Book title must be unique", {
          invalidArgs: args.title,
        });
      }

      const book = { ...args, id: uuid() };
      const currentAuthor = args.author;

      authors.filter((author) => author.name === currentAuthor).length === 0 &&
        (authors = authors.concat({
          name: currentAuthor,
          born: null,
          bookCount: 1,
        }));

      books = books.concat(book);
      return book;
    },

    editBorn: (root, args) => {
      const author = authors.find((author) => author.name === args.name);
      if (!author) {
        return null;
      }

      const updatedAuthor = { ...author, born: args.born };
      authors = authors.map((author) =>
        author.name === args.name ? updatedAuthor : author
      );
      return updatedAuthor;
    },
  },
};

const server = new ApolloServer({
  typeDefs,
  resolvers,
});

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`);
});
